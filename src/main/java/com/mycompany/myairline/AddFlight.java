    private String AutoId()
    {
        String ans="";
        // connection ctreation 
        try{
           Class.forName("com.mysql.cj.jdbc.Driver");
           Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline_reservation_system", "root", "shivani@1234");
           
           Statement st = con.createStatement();
            ResultSet set = st.executeQuery("select MAX(FlightId) from flight");
            set.next();
            if(set.getString("MAX(FlightId)")== null)
            {
                ans= "FL001";
            }
            else
            {
                String temp=set.getString("MAX(FlightId)");
                int n=temp.length();
                Long num = Long.parseLong(temp.substring(2, n));
                num++;
                ans= "FL"+String.format("%03d", num);
            }
              
         
       }
       catch(Exception e)
       {
           JOptionPane.showMessageDialog(null, e.toString());
       }
        return ans;
        
    }
